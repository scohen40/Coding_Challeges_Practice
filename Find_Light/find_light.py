#FIRST ATTEMPT
def find_light_1(image):
  # search each row from the bottom right for a 1
  for row in range(len(image)-1, 0, -1):
    for col in range(len(image[row])-1, 0, -1):
      if image[row][col] == 1:
        return [col, row]

